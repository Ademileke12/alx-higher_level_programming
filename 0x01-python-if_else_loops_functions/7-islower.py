#!/usr/bin/python3
def islower(c):
    return c.islower()


if islower('a') is True:
    print("a is lower")
else:
    print("a is upper")
if islower('H') is True:
    print("H is lower")
else:
    print("H is upper")
if islower('A') is True:
    print("A is lower")
else:
    print("A is upper")
if islower('3') is True:
    print("3 is lower")
else:
    print("3 is upper")
if islower('g') is True:
    print("g is lower")
else:
    print("g is upper")
